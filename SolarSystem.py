# Using turtle!
# Made is Visual Studio Code

from turtle import *
# Part 1
bgcolor('Black')

speed(0)

# Part 2
begin_fill()
color('Orange')
circle(60)
end_fill()

# Part 3
penup()
forward(100)
color('gray')
pendown()

# Part 4
begin_fill()
circle(20)
end_fill()

# Part 5
penup()
forward(100)
color('Red')
pendown()

# Part 6
begin_fill()
circle(40)
end_fill()

# Part 7
penup()
forward(90)
color('Green')
pendown()

# Part 8
begin_fill()
circle(30)
end_fill()

#Hides pen at end of animation
penup()

done()